    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        Almacen alm = new Almacen();
         boolean resultado = alm.eliminarAlmacen(Integer.parseInt(txtIdAlmacen.getText()));
        if(resultado==true){
            JOptionPane.showMessageDialog(null, "Se elimino el Almacen");
            this.modeloTabla.getDataVector().clear();
            cargarTabla();
        }else{
            JOptionPane.showMessageDialog(null, "No se elimino el Almacen");
        } 
    }//GEN-LAST:event_btnEliminarActionPerformed

